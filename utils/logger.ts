class Logger {
  log(...messages: any[]) {
    console.log(
      `
------------------------------------------------------------------------------

    LOG FROM LOGGER:
        `,
      ...messages,
      `
    ${new Date().toISOString()}
        
------------------------------------------------------------------------------`
    );
  }

  error(...messages: any[]) {
    console.error(
      `
------------------------------------------------------------------------------

    ERROR FROM LOGGER:
        `,
      ...messages,
      `
    ${new Date().toISOString()}
        
------------------------------------------------------------------------------`
    );
  }

  info(...messages: any[]) {
    console.info(
      `
------------------------------------------------------------------------------

    INFO FROM LOGGER:
        `,
      ...messages,
      `
    ${new Date().toISOString()}

------------------------------------------------------------------------------`
    );
  }

  warn(...messages: any[]) {
    console.warn(
      `

------------------------------------------------------------------------------
    WARN FROM LOGGER:
        `,
      messages,
      `
    ${new Date().toISOString()}`,
      `
------------------------------------------------------------------------------`
    );
  }
}

const logger = new Logger();
export default logger;
